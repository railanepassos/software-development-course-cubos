let velociadadeMediaKilometrosHora, distanciaPercorridaMetros = 1000, tempoSegundos = 3600;

velociadadeMediaKilometrosHora = (distanciaPercorridaMetros / tempoSegundos) * 3.6;

console.log(`Ao percorrer uma distância de ${distanciaPercorridaMetros} metros em ${tempoSegundos} segundos significa que você obteve uma velocidade média de ${velociadadeMediaKilometrosHora} Km/h`)